#extend(), append(), insert(id, val), remove(val), clear(), sort(), reverse(), copy()
list = ["Emma", "Jon", "Bob"]
list.extend("Liza")
print(list)

list = ["Emma", "Jon", "Bob"]
list.append("Liza")
print(list)

list = ["Emma", "Jon", "Bob"]
list.insert(1, "Liza")
print(list)


list = ["Emma", "Jon", "Bob"]
list.remove("Jon")
print(list)


list = ["Emma", "Jon", "Bob"]
list.clear()
print(list)

list = ["Emma", "Jon", "Bob"]
list.sort()
print(list)

list = ["Emma", "Jon", "Bob"]
list.reverse()
print(list)

list = ["Emma", "Jon", "Bob"]
list.copy()
print(list)



